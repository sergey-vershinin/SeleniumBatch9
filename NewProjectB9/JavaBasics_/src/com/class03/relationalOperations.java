package com.class03;

public class relationalOperations {
	public static void main(String[] args) {
		
		//>, < ,>=, <=, ==
		int a = 10;
		int b = 7;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
		
		
		System.out.print("\n");
				
		boolean result = a>=b;
		boolean result1 = a<=b;
		
		System.out.println(result);
		System.out.println(result1);
		
		System.out.println();
		
		int c = 20;
		int d = 29;
		
		System.out.println(c==d);
		System.out.println(c=d);
		
	}
}
