public class HW {

	public static void main(String[] args) {
		// print square of numbers
		
		for (int i=5; i>0; i-- ) {
			for(int j=0; j<5; j++) {
				System.out.print(i);
			}
			System.out.println();
		}
		
		System.out.println("-------------------");
		
		
		for (int i=5; i>0; i-- ) {
			for(int j=7; j>0; j--) {
				System.out.print(j);
			}
			System.out.println();
		}
		
	}
}
