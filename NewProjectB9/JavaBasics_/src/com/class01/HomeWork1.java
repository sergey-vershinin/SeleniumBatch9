package com.class01;

public class HomeWork1 {
	
	// main method
	public static void main (String[] args) {
	
		/*
		 * primitive data type
		 * numeric (integers)
		 */
		
		byte a = 1;
		short b = 235;
		int c = 23432423;
		long d = 241242141249898L;
		
		//Print out statements:
		System.out.println("Primitive data types");
		System.out.println("Integers: ");
		System.out.println("byte is " + a + ";");
		System.out.println("short is " + b + ";");
		System.out.println("integer is " + c + ";");
		System.out.println("long is " + d + ";");
				
		//numeric(floating point):
		
		double e = 12.34;
		float f = 132.123231f;
		
		//Print out statements:
		System.out.println("double is " + e + ";");
		System.out.println ("float is " + f + ";");
		
		/*
		 * Non-numeric data types:
		 * Characters
		 */
		
		//Declare char variable "k", "l", "m":
		
		char k = 'w';
		char l = '%';
		char m = '8';
		
		//Print out statement:
		
		System.out.println("Non-numeric data type: ");
		System.out.println("charecters: ");
		System.out.println("char 'k' is " + k + ";");
		System.out.println("char 'l' is " + l + ";");
		System.out.println("char 'm' is " + m + ";");
				
		//Declare boolean;
		
		boolean bool_1 = true;
		boolean bool_2 = false;
		
		//Print out statement:
		
		System.out.println("boolean one is " + bool_1 + ";");
		System.out.println("boolean two is " + bool_2 + ".");		
	}
}
